package pl.edu.agh.servicetracker.auth;

public class UserCredentials {
    
    private String email;

    private String token;

    public UserCredentials(String email, String token) {
        this.email = email;
        this.token = token;
    }

    public String getEmail() {
        return email;
    }

    public String getToken() {
        return token;
    }
}
